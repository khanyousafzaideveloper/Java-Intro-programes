                                                                                                                                                     public class BSStudent extends Student{

    int semester;
    double cgpa;

    public void set_semester(int s)
    {
        semester = s;
    }
    public void set_cgpa(double c)
    {
        cgpa = c;
    }
    public int get_semester()
    {
        return semester;
    }
    public double get_cgpa()
    {
        return cgpa;
    }
    
}

